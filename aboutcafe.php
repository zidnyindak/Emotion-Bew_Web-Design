    <h2>Menu Khas</h2>
    <p>
        Menu di Café Emotion Brew dikurasi dengan baik untuk mencerminkan berbagai macam perasaan, mulai dari minuman yang segar hingga makanan yang menggugah selera. Beberapa menu andalan kami adalah:
    </p>
    <ul>
        <li><strong>Joyful Pan Delight</strong> - Pancake yang lembut dan manis, melambangkan rasa bahagia yang meluap.</li>
        <li><strong>Cupcake Inside Out</strong> - Cupcake warna-warni dengan perasaan yang berlapis, melambangkan kompleksitas emosi manusia.</li>
    </ul>
</section>

