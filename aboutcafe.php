    <h2>Lokasi dan Jam Operasional</h2>
    <p>
        Café Emotion Brew berlokasi di pusat kota, mudah diakses dari berbagai penjuru. Kami buka setiap hari dari jam 08:00 pagi hingga 11:00 malam. Lokasi kami yang strategis dan suasana yang nyaman menjadikan café ini tempat yang sempurna untuk berkumpul bersama teman, keluarga, atau bahkan untuk bekerja atau belajar.
