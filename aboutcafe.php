<section>
    <h2>Konsep dan Desain</h2>
    <p>
        Café EMotion Brew mengusung konsep yang terinspirasi dari film animasi terkenal "Inside Out", di mana setiap elemen desain interior merefleksikan berbagai macam emosi: kebahagiaan, kesedihan, kemarahan, rasa takut, dan rasa jijik. Warna-warna cerah dan dekorasi tematik menjadikan café ini sebagai tempat yang nyaman dan Instagrammable. Setiap sudut café dirancang dengan cermat untuk menciptakan pengalaman visual yang memikat, dari dinding berwarna-warni hingga meja-meja unik yang menggambarkan karakter emosi.
    </p>
